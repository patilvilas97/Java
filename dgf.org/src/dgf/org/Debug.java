package dgf.org;

public class Debug {
	int a=10;
	boolean b=true;
	float f=120;

	public static void main (String[] args ) {
		int a=20;
		char c=34;
		
		Debug obj=new Debug();
		System.out.println("a="+obj.a);
		System.out.println("b="+obj.b);
		System.out.println("f="+obj.f);
		System.out.println("a="+a);
		System.out.println("c="+c);
		
		
		
	}
}
